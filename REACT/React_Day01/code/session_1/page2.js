// variables
// - let: variable
// - const: constant
// - data types are inferred

// number
const num = 100
console.log(`num = ${num}, type = ${typeof num}`)

// number
const salary = 10.5
console.log(`salary = ${salary}, type = ${typeof salary}`)

// string
const firstName = 'Steve'
console.log(`first name = ${firstName}, type = ${typeof firstName}`)

// boolean
const canVote = true
console.log(`canVote = ${canVote}, type = ${typeof canVote}`)

// object
const person = { name: 'person1', address: 'pune' }
console.log(`type = ${typeof person}`)
console.log('person = ', person)

// undefined
let myvar
console.log(`myVar = ${myvar}, type = ${typeof myvar}`)
